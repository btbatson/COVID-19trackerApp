public class Cases {
    String country;
    String todayCases;
    String deaths;
    String recovered;
    String critical;
    String todayDeaths;
    String image;
    String cases;

    public Cases() {
    }

    public String getName() {
        return country;
    }

    public String gettodayCases() {
        return todayCases;
    }
    public String getdeaths() {
        return deaths;
    }
    public String getrecovered() {
        return recovered;
    }
    public String getcritical() {
        return critical;
    }
    public String gettodayDeaths() {
        return todayDeaths;
    }
    public String getPhone() {
        return cases;
    }
}
